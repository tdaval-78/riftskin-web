create or replace function public.get_public_update_notice(p_channel text default 'stable')
returns table(
  channel text,
  latest_version text,
  minimum_version text,
  message text,
  mandatory boolean,
  enabled boolean,
  published_at timestamptz
)
language sql
stable
security definer
set search_path = public
