    return query select false, 'not_admin';
    return;
  end if;

  if v_state not in ('ok', 'maintenance') then
    v_state := 'maintenance';
  end if;

  insert into public.app_service_status (
    channel,
    injection_state,
    service_message,
    published_at,
    updated_at,
    updated_by
  ) values (
    v_channel,
    v_state,
    v_message,
    now(),
    now(),
    v_admin
  )
  on conflict (channel)
  do update set
    injection_state = excluded.injection_state,
    service_message = excluded.service_message,
    published_at = now(),
    updated_at = now(),
    updated_by = v_admin;

  return query select true, 'published';
end;
$$;

